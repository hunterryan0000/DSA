package TreeTraversal;

// Class definition for a TreeNode in a binary tree
class TreeNode {

    // 'val' stores the integer value for this node
    int val;

    // 'left' is a reference pointing to the left child node
    TreeNode left;

    // 'right' is a reference pointing to the right child node
    TreeNode right;

    // Constructor for TreeNode class
    TreeNode(int x) {
        // Initialize 'val' field with the provided value 'x'
        val = x;
    }
}

